    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        TenisDAO dao = new TenisDAO();
        for (Tenismodel t : dao.read()) {
            modelo.addRow(new Object[]{
                t.getId(),
                t.getNome(),
                t.getMarca(),
                t.getCor(),
                t.getTamanho(),
                t.getPreco()
            });
        }
    }
    
