    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() != -1) {
            Id.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            Nome.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
            Marca.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());
            Cor.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString());
            Tamanho.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 4).toString());
            Preco.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 5).toString());
        }
        
        
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() != -1) {
            int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            TenisDAO dao = new TenisDAO();
            dao.delete(id);
            limparCampos();
            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um tênis para excluir.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (Nome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nome obrigatório");
            return;
        }
        Tenismodel t = new Tenismodel();
        TenisDAO dao = new TenisDAO();
        t.setNome(Nome.getText());
        t.setMarca(Marca.getText());
        t.setCor(Cor.getText());
        try {
            t.setTamanho(Integer.parseInt(Tamanho.getText()));
        } catch (Exception e) {
            t.setTamanho(0);
        }
        try {
            t.setPreco(Double.parseDouble(Preco.getText()));
        } catch (Exception e) {
            t.setPreco(0.0);
        }
        dao.create(t);
        limparCampos();
        readJTable();
    }//GEN-LAST:event_jButton1ActionPerformed



