    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
if (jTextField2.getText().isEmpty() || jTextField4.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Nome e Preço obrigatórios");
        return;
    }
    doce doce = new doce();
    doceDAO dao = new doceDAO();
    doce.setnomedoce(jTextField2.getText());
    doce.setcategoria(jTextField3.getText());    
    doce.setpreco(Float.parseFloat(jTextField4.getText()));
    doce.setdesc(jTextArea1.getText());
    dao.create(doce);
    limparCampos();
    readJTable();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    if (jTable1.getSelectedRow() != -1) {
        doce doce = new doce();
        doceDAO dao = new doceDAO();
        doce.setId(Integer.parseInt(jTextField1.getText()));
        doce.setnomedoce(jTextField2.getText());
        doce.setcategoria(jTextField3.getText());
        doce.setpreco(Float.parseFloat(jTextField4.getText()));
        doce.setdesc(jTextArea1.getText());
        dao.update(doce);
        limparCampos();
        readJTable();
    } else {
        JOptionPane.showMessageDialog(null, "Selecione doce para atualizar");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() != -1) {
        int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
        doceDAO dao = new doceDAO();
        dao.delete(id);
        limparCampos();
        readJTable();
    } else {
        JOptionPane.showMessageDialog(null, "Selecione um pet para excluir.");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

