        public void readJTable() {
    DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
    
    modelo.setNumRows(0);
    
    doceDAO ddao = new doceDAO();
    
    for (doce d : ddao.read()) {
        // Adiciona cada pet na tabela
        modelo.addRow(new Object[]{
            d.getId(),
            d.getnomedoce(),
            d.getcategoria(),
            d.getpreco(),
            d.getdesc()
        });
    }
}

